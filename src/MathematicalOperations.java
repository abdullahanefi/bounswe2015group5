public class MathematicalOperations {
    /**
     * Performs addition of x and y.
     * Added by Can Guler.
     * @param x First operand
     * @param y Second operand
     * @return Sum of x and y
     */
    public static int binaryPlus(int x, int y) {
        return x+y;
    }
}
